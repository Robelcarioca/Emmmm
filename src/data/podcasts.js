export function getMockEpisodes(podcast) {
  const titles = [
    `Latest: ${podcast.title} — New Episode`,
    `${podcast.host} on AI & the Future`,
    `Deep Dive: The Science of Excellence`,
    `${podcast.host} Reflects on 2024`,
    `Q&A Special: Listener Questions`,
  ];
  return titles.map((title, i) => ({
    id:       `${podcast.id}-ep-${i}`,
    title,
    duration: `${1 + Math.floor(Math.random() * 2)}h ${10 + Math.floor(Math.random() * 50)}min`,
    audioUrl: null,
    youtubeId: podcast.youtubeId,
    rssFeed:  podcast.rssFeed,
    rssIndex: i,
  }));
}
